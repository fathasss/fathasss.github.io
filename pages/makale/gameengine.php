      <ul class="w3-ul w3-border w3-white w3-text-grey">
		<li class="w3-padding"><p style="text-align:center" class="w3-bar-item"></p>
		<h4 style="text-align:center"><b>OYUN MOTORLARI</b></h4>
		</li>
		<li class="w3-padding"><p style="text-align:center" class="w3-bar-item"></p>
		Bilgisayar, mobil cihazlar ve konsol sistemleri üzerinde envanterleri önceden belirlenmiş, planlanmış oyunları yaratmaya yarayan yazılımlardır.
        Ücretli ve ücretsiz versiyonları bulunan oyun motorları genel olarak C++ ve C# programlama dilleriyle geliştirilmiştir. 
        2D ve 3D özelliklerine göre kullanımında farklılık gösteren oyun motorları günümüz oyun sektörünün vazgeçilmezleridir.
        Oyun motorları, esasında yazılım kütüphanelerinden oluşmuş bir yapıdır. 
        Bu kütüphanelerin içinde tanımlanmış fonksiyonlar, sınıflar vb. gibi yapılar bulunmaktadır. 
        Geliştirici oyun geliştirirken daha önceden tasarladığı ve oyun motorunun kütüphanesine eklediği bu verileri kullanır. 
        Oyun motorlarının kişiye sağladığı fayda; kişiyi önceden başkası tarafından kodlanarak tanımlanmış olan bu verileri yeniden kodlama 
        zahmetinden kurtarması ve bunun sonucunda daha az kod ve bunun getirdiği daha az karmaşıklık ve aynı zamanda, zamandan tasarruf etmesini sağlar.
        Şimdi kalite bakımından kendimce en iyi olan ilk 10 oyun motorundan bahsetmek istiyorum.
        <p><b>1.Frostbite Engine</b></p><br>
        <img style="align:center;max-width:100%;height:auto;" src="/images/makale-resim/Frostbite.jpg">
        <p>Digital Illusions CE (Electronic Arts)  tarafından birincil şahış nişancı  (First Person Shooter) oyun türleri için çıkartılan oyun motorudur. 
        Battlefield serisi, Dragon Ace, Fifa 17 ve 18,
        Medal of  Honnor (multiplayer), Need For Speed: The Run, Star Wars Battlefront ve Battlefront 2 gibi oyunlar frostbite oyun motoruyla geliştirilmiştir. 
        3D oyunlarda bayağı iddialı olan Frostbite Engine fizik konusunda, gölgelendirme, yapay zeka gibi konularda çok başarılıdır. 
        Çoğu oyun geliştirici ve otoriteler tarafından en başarılı oyun motoru olarak gösterilir. 
        Şirket dışındaki geliştiricilerle ücretle dahi paylaşılmayan oyun motorudur.</p>
        <p><b>2.Unreal Engine</b></p><br>
        <img style="align:center;max-width:100%;height:auto;" src="/images/makale-resim/unreal.png">
        <p>Epic Games tarafından FPS tarzı oyunlar için geliştirilen ve 1998 yılında yayınlanan oyun motorudur. 
        FPS için geliştirilmesine rağmen daha sonra çeşitli oyun kategorileri için kullanıldı. 
        (Third Person Shooter) Microsoft Windows, Xbox One, Windows RT için DirectX kullanır. OS X, Play Station 4, IOS ve Android için OpenGL ve web tarayıcıları için JavaScript/WebGL kullanır. 
        İlk 3 sürümü ücretli yayınlanan Unreal Engine 4 sürümüyle tamamen geliştiricilere açılmıştır.
        </p>
        <p><b>3.Cry Engine</b></p><br>
        <img style="align:center;max-width:100%;height:auto;" src="/images/makale-resim/cryengine.jpg">
        <p>Göçmen Türk kardeşlerin kurduğu Crytek şirketinin kullandığı oyun motorudur (Aynı zamanda şuanda kullandığım oyun motoru). 
        GFACE arayüzü üzerinden indirilen CryEngine, Crytek ve Ubisoft ile ortak olarak geliştirdiği oyun motorudur. 
        3 boyut özelliğinin çok iyi olması ve ücretsiz olması nedeniyle tercih edilen oyun moturunun ilk oyunu FarCry' dır. 
        Sandbox düzenleyicisiyle tüm platformlar üzerinde kullanılabilir. Crysis serisi bu motorla geliştirilmiştir.
        </p>
        <p><b>4.Anvil Engine</b></p><br>
        <img style="align:center;max-width:100%;height:auto;" src="/images/makale-resim/anvilengine.jpg">
        <p>Anvil Engine (2008'den önceki sürümüyle Scimitar), Ubisoft oyun şirketi tarafından geliştirilen oyun motorudur. 
        Microsoft Windows , Play Station 3, Xbox One ve Wii U konsolları üzerinde oyun geliştirmek için kullanılır. 
        Frostbite gibi lisansı kapalı kaynaktır. 
        Assasins Creed 2, Prince of Persia :The Forgetten Sands, Assasins Creed :Brotherhood, Assasins Creed :Revelations ve Tom Clancy's Raimbow 6 :Patriots 
        oyunları Anvil Engine ile geliştirilmiştir.
        </p>
        <p><b>5.IW Engine</b></p><br>
        <img style="align:center;max-width:100%;height:auto;" src="/images/makale-resim/infinity.jpg">
        <p> C, C++ ve Python programlama diliyle İnfinity Ward ve Treyach tarafından geliştirilen meşhur oyun motorudur. 
        İlk olarak 2005 yılında yayınlanan Call of Duty 2 bu oyun motoruyla geliştirilmiştir. 
        Call of Duty oyunlarının gelişmesiyle birlikte yeni sürümleri kullanılanılmıştır. 
        Şuanki güncel sürümü IW Engine 8.0 CAll of Duty' nin 2019 yılında çıkacak olan oyununda kullanılmaktadır.
        </p>
        <p><b>6.RAGE (Rockstar Advanced Game Engine)</b></p><br>
        <img style="align:center;max-width:100%;height:auto;" src="/images/makale-resim/rage.jpg">
        <p> Rockstar North' a katkı sağlamak amacıyla RAGE Teknology Group adı altında küçük bir ekibin oyun geliştirici şirket 
        Rockstar San Diego' sa oluşturulduğu kapalı lisanlı oyun motorudur.Oyun geliştirmeyi kolaylaştırmak için bilgisayar, 
        PlayStation, Wii ve Xbox 360 konsolları için geliştirilmiştir.
        </p>
        <p><b>7. STEM Engine</b></p><br>
        <img style="align:center;max-width:100%;height:auto;" src="/images/makale-resim/stem.jpg">
        <p>Yeni bir oyun motoru olan STEM Engine Bethesda bünyesinde bulunan bir diğer firma olan id Software’in Doom ve Wolfenstein gibi oyunlarında kullandığı id Tech motoru ile geçlendirilmiştir. 
        Yeni piyasaya sürülen The Evil Within 2 oyununda kullanılan oyun motorudur. 
        Çok başarılı 3D özelliği, gölgelendirme özelliği --bir o kadar da kararsız olmasına-- rağmen gayet iyi ve etkili bir motordur.
        </p>
        <p><b>8. Decima Engine</b></p><br>
        <img style="align:center;max-width:100%;height:auto;" src="/images/makale-resim/decima.jpg">
        <p>Decima, 15 Kasım 2013'te yayınlanan Guerrilla Games ve Kojima Productions tarafından geliştirilen özel bir oyun motorudur. 
        4K ve HDR (High Dinamic Range) ile uyumluluğun yanı sıra yapay zeka, fizik, mantık ve dünyalar yaratmak için araç ve özellikler barındırır. 
        Killzone Shadow Fall, Şafak Vakti, RIGS: Mekanize Savaş Ligi ve Horizon Sıfır Şafak'ta olduğu gibi yaklaşmakta olan Death Stranding oyunlarının da en iyi bilinen ismidir. 
        Ayrıca Death Stranding oyununda görüntü işleme özelliğini de kullanabilen mükemmel bir oyun motorudur.
        Mads Mikkelsen ve Norman Reedus gibi Hollywood karakterleri görüntü işleme özelliği ile oyuna aktarılmıştır. Lisansı Property' e aittir. 
        Şuanlık sadece PlayStation 4 üzerinde kullanılmaktadır. 
        </p>
        <p><b>9. Unity Engine</b></p><br>
        <img style="align:center;max-width:100%;height:auto;" src="/images/makale-resim/unity.jpg">
        <p>Unity, Unity Technologies tarafından geliştirilen ve bilgisayar, konsol ve mobil cihazlar için üç boyutlu ve iki boyutlu video oyunları 
        ve simülasyonları geliştirmek için kullanılan çapraz platform oyun motorudur. 
        İlk kez 2005 yılında Apple'ın Dünya Çapında Geliştirilen Geliştiricileri Konferansı'nda OS X için duyuruldu, o zamandan beri hedef 27 platform için genişletildi. 
        Birlik'in altı büyük versiyonu çıktı. Unity ile yapılan oyunlarını görmek için Unity' nin resmi sayfasını ziyaret edebilirsiniz.
        </p>
        <p><b>10. Fox Engine</b></p><br>
        <img style="align:center;max-width:100%;height:auto;" src="/images/makale-resim/fox.png">
        <p>Kojima Productions tarafından tasarlanan çok daha kısa geliştirme süresine sahip çoklu ortam platform oyunları yaratmak için tasarlanmıştır. 
        Metal Gear Solid ve Pro Evolotion Soccer gibi oyunların oyun motorudur. 2008 yılında yayınlanan Metal Gear Solid 4' ten sonra Dünyanın en iyi oyun motoru seçilmiştir.
        </p>
		</li>
        <li class="w3-padding"><p style="text-align:center" class="w3-bar-item"></p>
